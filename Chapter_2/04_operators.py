print(sub + 2)  # Addition Assignment
sub += 2
print(sub)  # Addition Assignment
print(sub - 2)  # Subtraction Assignment
sub -= 2
print(sub)  # Subtraction Assignment
print(sub * 2)  # Multiplication Assignment
sub *= 2
print(sub)  # Multiplication Assignment
print(sub / 2)  # Division Assignment
sub /= 2
