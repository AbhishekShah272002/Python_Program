# Operators in Python
# Operators are special symbols that perform operations on variables and values.
# Arithmetic Operators
# +, -, *, /, //, %, **

a = 7
b = 3
c = 2
print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # Multiplication
print(a / b)  # Division
print(a // b)  # Floor Division
print(a % b)  # Modulus
print(a ** c)  # Exponentiation
print(a + b * c)  # Operator Precedence
print((a + b) * c)  # Parentheses to change precedence
print(a + b - c * a / b)  # Complex Expression
print(a + b - c * a // b)  # Complex Expression with Floor Division
print(a + b - c * a % b)  # Complex Expression with Modulus
print("\n" )  # New line for better readability

#Assignment Operators
print("Assignment Operators")
sub = 5-2
