# Variable Naming Rules in Python
# Variable names are case-sensitive and can be any length. They can also include letters, numbers, and underscores.
a = 23

aqa= 2

abhi = 3

sam = 4

_abhi = 5

# @bhi_ = 6
# a@bhi = 7

# 1. Variable names can only contain letters, numbers, and underscores.
# 2. Variable names cannot start with a number.
# 3. Variable names cannot contain spaces or special characters (except for underscores).
# 4. Variable names are case-sensitive (e.g., "myVar" and "myvar" are different variables).
# 5. Variable names cannot be the same as Python keywords (e.g., "if", "else", "while", etc.).